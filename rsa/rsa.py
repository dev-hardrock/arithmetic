# 随机取一数e, 使得 1 < e < f_n, 并且 e 和 f_n 互质， 这里取 e = 5
e = 5

# 找一个数 d, 使得 ed = 1(mod f_n), ed = K*f_n +1, K >=1;
d = 173  # 满足 e*d = f_n * 3 +1

# 公钥为(5,323) , 私钥为115

# 明文 m = 123
m = 123
